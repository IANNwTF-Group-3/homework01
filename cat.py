class Cat:
    def __init__(self, name):
        self.name = name

    # greet each other by first introducing themselves and then addressing a different cat by name
    def greet(self, other):
        print(f"Hi, I'm {self.name}, the fluffiest of them all! Just must be {other.name}, a pleasure to meet you! I found some mice for us to play with, let's go!")


    
